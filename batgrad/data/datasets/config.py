from __future__ import annotations

from dataclasses import dataclass, field
from typing import TYPE_CHECKING

from batgrad.data.processing.interactive import (
    InteractiveStageRun,
    run_load_interactive,
    run_load_interactive_manifest,
)
from batgrad.data.processing.normalize import (
    NormalizeStageConfig,
    NormalizeStageSpec,
    run_normalize,
    run_normalize_interactive,
)
from batgrad.data.processing.raw import IngestStageConfig, IngestStageSpec, run_ingest

if TYPE_CHECKING:
    import polars as pl

    from batgrad.contracts.mapping import DatasetStageId, DatasetTypeId
    from batgrad.data.processing.normalize import NormalizeTask
    from batgrad.data.processing.raw import IngestTask, RawDatasetAdapter
    from batgrad.storage.store import DataProcessingStore

STAGE_SPECS = IngestStageSpec | NormalizeStageSpec
DatasetId = str


@dataclass(frozen=True)
class DatasetInfo:
    """Human-readable dataset metadata used by registry entries.

    Attributes:
        name: Display name.
        year: Publication or release year.
        author: Primary author or source organization.
        parent_dataset_id: Registry id of the source dataset when this dataset is
            derived from another one.
        misc: Additional source-specific metadata.
        description: Optional longer description.
    """

    name: str | None = None
    year: int | None = None
    author: str | None = None
    parent_dataset_id: DatasetId | None = None
    misc: dict[str, object] = field(default_factory=dict)
    description: str | None = None


@dataclass(frozen=True)
class DatasetSpec:
    """Storage layout and processing-stage configuration for one dataset.

    `processing_stages` maps stage ids to ingest and normalize specs. Store paths
    are relative; the configured `DataProcessingStore` resolves them to local or
    remote locations.

    Attributes:
        dataset_id: Stable registry and storage id.
        dataset_type: Dataset origin/type label.
        info: Optional human-readable metadata.
        processing_stages: Stage configuration keyed by stage id.

    Examples:
        >>> from batgrad.contracts.mapping import DatasetStageId, DatasetTypeId
        >>> spec = DatasetSpec(
        ...     dataset_id="my-dataset",
        ...     dataset_type=DatasetTypeId.published,
        ... )
        >>> spec.manifest(DatasetStageId.ingested)
        'type=published/dataset=my-dataset/source=ingested/manifest.parquet'
    """

    dataset_id: DatasetId
    dataset_type: DatasetTypeId
    info: DatasetInfo | None = None
    processing_stages: dict[DatasetStageId, STAGE_SPECS] = field(default_factory=dict)

    @property
    def root(self) -> str:
        """Relative root location of the dataset.

        Absolute root location will be resolved by `DataStore`.

        Returns:
            Relative dataset root path.
        """
        return f"type={self.dataset_type}/dataset={self.dataset_id}"

    def source_root(self, source: DatasetStageId) -> str:
        """Relative root for one dataset stage.

        Args:
            source: Stage id.

        Returns:
            Relative store root for files generated by that stage.
        """
        return f"{self.root}/source={source}"

    def source_file(self, source: DatasetStageId, file_name: str) -> str:
        """Relative file path inside one dataset stage.

        Args:
            source: Stage id.
            file_name: File name relative to the stage root.

        Returns:
            Relative store path for the stage file.
        """
        return f"{self.source_root(source)}/{file_name}"

    def manifest(self, source: DatasetStageId) -> str:
        """Relative manifest path for one dataset stage.

        Args:
            source: Stage id.

        Returns:
            Relative path to the stage manifest parquet file.
        """
        return self.source_file(source, "manifest.parquet")


@dataclass(frozen=True)
class Dataset:
    """Registered dataset bundle and main processing interface.

    `Dataset` combines a `DatasetSpec` with an optional raw adapter. Use it when
    running standard ingest/normalize workflows from registry entries. A dataset
    without `raw_adapter` cannot ingest raw files but may still support normalize
    or interactive loading from existing manifests.

    Attributes:
        spec: Dataset storage layout and stage configuration.
        raw_adapter: Optional raw-file adapter used by `ingest`.

    Examples:
        >>> dataset = get_dataset("pozzato-2022")
        >>> dataset.spec.dataset_id
        'pozzato-2022'
    """

    spec: DatasetSpec
    raw_adapter: RawDatasetAdapter | None = None

    def ingest(
        self,
        input_store: DataProcessingStore,
        output_store: DataProcessingStore,
        config: IngestStageConfig,
        *,
        scratch_store: DataProcessingStore | None = None,
        tasks: tuple[IngestTask, ...] | None = None,
    ) -> None:
        """Run this dataset's raw-to-ingested parquet stage.

        Args:
            input_store: Store containing raw source files.
            output_store: Store receiving ingested shards and manifest.
            config: Ingest runtime and parquet-writing settings.
            scratch_store: Optional temporary-output store. Defaults to
                `output_store`; use a separate scratch store when final output
                should not hold intermediate task files.
            tasks: Optional task subset for retries, debugging, or tests. When
                omitted, the raw adapter plans tasks from source files.

        Raises:
            TypeError: If this dataset has no raw adapter.
        """
        if self.raw_adapter is None:
            raise TypeError(f"Dataset {self.spec.dataset_id!r} does not support ingest")
        run_ingest(
            self.raw_adapter,
            input_store,
            output_store,
            config,
            scratch_store=scratch_store,
            tasks=tasks,
        )

    def normalize(
        self,
        input_store: DataProcessingStore,
        output_store: DataProcessingStore,
        config: NormalizeStageConfig,
        *,
        scratch_store: DataProcessingStore | None = None,
        tasks: tuple[NormalizeTask, ...] | None = None,
        dry_run: bool = False,
    ) -> None:
        """Run this dataset's ingested-to-normalized parquet stage.

        Args:
            input_store: Store containing ingested manifest and shards.
            output_store: Store receiving normalized shards and manifest.
            config: Normalize runtime, validation, and parquet-writing settings.
            scratch_store: Optional temporary-output store. Defaults to
                `output_store`.
            tasks: Optional pre-planned task subset for retries, debugging, or
                tests. When omitted, tasks are planned from the ingested manifest.
            dry_run: Validate task preparation and checks without writing final
                normalized outputs.
        """
        run_normalize(
            self.spec,
            input_store,
            output_store,
            config,
            scratch_store=scratch_store,
            tasks=tasks,
            dry_run=dry_run,
        )

    def normalize_interactive(
        self,
        input_store: DataProcessingStore,
        scratch_store: DataProcessingStore,
        config: NormalizeStageConfig,
        *,
        protocols: object = None,
        group_values: object = None,
        annotate: bool = True,
        source_run: InteractiveStageRun | None = None,
        normalize_spec: NormalizeStageSpec | None = None,
    ) -> InteractiveStageRun:
        """Run selected normalization work into scratch storage for exploration.

        Args:
            input_store: Store containing ingested or source interactive data.
            scratch_store: Store receiving temporary interactive outputs.
            config: Normalize runtime, validation, and parquet-writing settings.
            protocols: Optional protocol selector, such as one protocol id or a
                list of protocol ids.
            group_values: Optional task group selector or list of selectors.
                Keys are group column names such as `cell id` or `cycle index`.
            annotate: Whether public annotation columns are included in outputs.
            source_run: Optional prior unresampled normalized interactive run to
                reuse when trying resampling changes.
            normalize_spec: Optional normalization spec override.

        Returns:
            Interactive run handle for scanning outputs and cleaning scratch
            files.
        """
        return run_normalize_interactive(
            self.spec,
            input_store,
            scratch_store,
            config,
            protocols=protocols,
            group_values=group_values,
            annotate=annotate,
            source_run=source_run,
            normalize_spec=normalize_spec,
        )

    def load_interactive(
        self,
        input_store: DataProcessingStore,
        *,
        source: DatasetStageId | str,
        protocols: object = None,
        group_values: object = None,
    ) -> InteractiveStageRun:
        """Load selected rows from an existing stage manifest as an interactive run.

        Args:
            input_store: Store containing the stage manifest and data shards.
            source: Stage id to load, such as `DatasetStageId.ingested`.
            protocols: Optional protocol selector.
            group_values: Optional task group selector or list of selectors.

        Returns:
            Interactive run handle over selected existing stage data.
        """
        return run_load_interactive(
            self.spec,
            input_store,
            source=source,
            protocols=protocols,
            group_values=group_values,
        )

    def load_interactive_manifest(
        self,
        input_store: DataProcessingStore,
        *,
        source: DatasetStageId | str,
        manifest: pl.DataFrame,
        protocols: object = None,
        group_values: object = None,
    ) -> InteractiveStageRun:
        """Wrap an already-loaded manifest as an interactive run.

        Args:
            input_store: Store containing the stage data shards referenced by the
                manifest.
            source: Stage id the manifest belongs to.
            manifest: Manifest rows to expose interactively.
            protocols: Optional protocol selector used for protocol order.
            group_values: Optional task group selector retained on the run.

        Returns:
            Interactive run handle over the provided manifest rows.
        """
        return run_load_interactive_manifest(
            self.spec,
            input_store,
            source=source,
            manifest=manifest,
            protocols=protocols,
            group_values=group_values,
        )
